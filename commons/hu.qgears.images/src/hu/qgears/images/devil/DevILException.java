package hu.qgears.images.devil;

public class DevILException extends RuntimeException{
	private static final long serialVersionUID = 1L;

	public DevILException() {
		super();
	}

	public DevILException(String message, Throwable cause) {
		super(message, cause);
	}

	public DevILException(String message) {
		super(message);
	}

	public DevILException(Throwable cause) {
		super(cause);
	}

}
